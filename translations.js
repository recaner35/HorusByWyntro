    // --- 5. MODAL FONKSİYONLARI ---
    function openModal() {
        if (!modal) return;
        modal.style.display = 'flex'; 
        modal.classList.remove('hidden');
        
        requestAnimationFrame(() => {
            modal.classList.remove('opacity-0');
        });

        if(mobileMenu) mobileMenu.classList.add('opacity-0', 'pointer-events-none');
    }

    function closeModal() {
        if (!modal) return;
        modal.classList.add('opacity-0');
        setTimeout(() => {
            modal.classList.add('hidden');
            modal.style.display = 'none';
        }, 500);
    }

    // --- 6. TIKLAMA OLAYLARI ---
    langBtn.addEventListener('click', function(e) {
        e.preventDefault();
        e.stopPropagation(); 
        console.log("Dünya butonuna tıklandı!"); 
        openModal();
    });

    if (closeBtn) {
        closeBtn.addEventListener('click', function(e) {
            e.preventDefault();
            closeModal();
        });
    }

    // --- 7. DİL DEĞİŞTİRME MANTIĞI ---
    function setLanguage(langCode) {
        const data = translations[langCode] || translations['tr'];
        const fallback = translations['tr'];

        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (data && data[key]) {
                el.innerHTML = data[key];
            } else if (fallback && fallback[key]) {
                el.innerHTML = fallback[key];
            }
        });

        // Font Ayarları (Asya dilleri için)
        if (['zh', 'ja', 'ko'].includes(langCode)) {
            document.body.style.fontFamily = "'Noto Sans SC', 'Noto Sans JP', sans-serif";
        } else {
            document.body.style.removeProperty('font-family');
        }

        localStorage.setItem('horus_lang', langCode);
        closeModal();

        // GSAP ScrollTrigger Yenile (Metin uzunlukları değişince kaymayı önler)
        if (typeof ScrollTrigger !== 'undefined') {
            setTimeout(() => ScrollTrigger.refresh(), 500);
        }
    }

    // --- 8. BAŞLANGIÇ ---
    const savedLang = localStorage.getItem('horus_lang') || 'tr';
    setLanguage(savedLang);
